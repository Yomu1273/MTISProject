    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        username=jTextField1.getText();
        password=new String(jPasswordField1.getPassword());
        MTISforum m = new MTISforum();
        int flag=0;
        for(int i=0;i<m.i4;i++)
        {
            if(m.arr4[i].username.equalsIgnoreCase(username))
            {
                flag=1;
                String pass = m.arr4[i].getpaswd();
                if(pass.equalsIgnoreCase(password))
                {
                    m.s4.push(m.arr4[i]);
                    PlantationMain pm = new PlantationMain();
                    pm.setVisible(true);
                    this.setVisible(false);
                }
                else
                {
                    jLabel6.setText("Password is incorrect");
                }
            }
        }
        if(flag==0)
        {
            jLabel6.setText("Only Workers can access this field");
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

