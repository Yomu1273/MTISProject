    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        MTISforum m = new MTISforum();
        Visitors now=m.s3.peek();
        if(now.perks<30000f)
        {
            jLabel1.setText("Not enough Balance");
        }
        else
        {
            if(now.stayDays==7)
            {
                now.perks-=5000f;
                m.p3.add(now.username);
                this.setVisible(false);
            }
            if(now.stayDays==10)
            {
                now.perks-=7000f;
                m.p3.add(now.username);
                this.setVisible(false);
            }
            if(now.stayDays==14)
            {
                now.perks-=16000f;
                m.p3.add(now.username);
                this.setVisible(false);
            }
            if(now.stayDays==20)
            {
                now.perks-=20000f;
                m.p3.add(now.username);
                this.setVisible(false);
            }
            if(now.stayDays==30)
            {
                now.perks-=30000f;
                m.p3.add(now.username);
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

